package ssm.entity;

public class Standard {
	private int id;
	private String title_con;
	private int type;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle_con() {
		return title_con;
	}
	public void setTitle_con(String title_con) {
		this.title_con = title_con;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
}
